def fmt_val(value, fmt: str) -> str:
    if fmt == "pct":
        return f"{value*100:.2f}%"
    if fmt == "sec":
        return f"{value:.0f}秒"
    if fmt == "num":
        return f"{value:,.2f}" if value < 1000 else f"{value:,.0f}"
    if fmt == "money":
        return f"${value:,.2f}"
    return str(value)


